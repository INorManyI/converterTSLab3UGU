export function GetTimeUnitsOfMeasure()
{
    return ['year', 'month', 'week', 'day', 'hour', 'minute', 'second'];
}

export function GetLengthUnitsOfMeasure()
{
    return ['kilometer', 'meter', 'centimeter', 'millimeter'];
}

export function GetAreaUnitsOfMeasure()
{
    return ['sq_kilometer', 'sq_meter', 'sq_centimeter', 'sq_millimeter'];
}

export function GetWeightUnitsOfMeasure()
{
    return ['tonne', 'kilogram', 'gram', 'milligram', 'microgram'];
}

export function GetVolumeUnitsOfMeasure()
{
    return ['cubic_centimeter', 'cubic_millimeter', 'liter', 'milliliter'];
}
